class TestFirewallRuleModel:

    def test_src_network_cidr(self):
        rule = FirewallRule(
            rule_id="test-01", source="test", priority=0, line_number=1,
            src_ip="192.168.1.0/24", action=Action.ALLOW
        )
        net = rule.src_network()
        import ipaddress
        assert net == ipaddress.ip_network("192.168.1.0/24")

    def test_port_range_single(self):
        rule = FirewallRule(
            rule_id="test-02", source="test", priority=0, line_number=1,
            action=Action.ALLOW
        )
        assert rule.port_range("80") == (80, 80)

    def test_port_range_range(self):
        rule = FirewallRule(
            rule_id="test-03", source="test", priority=0, line_number=1,
            action=Action.ALLOW
        )
        assert rule.port_range("1024:65535") == (1024, 65535)

    def test_port_range_none(self):
        rule = FirewallRule(
            rule_id="test-04", source="test", priority=0, line_number=1,
            action=Action.ALLOW
        )
        assert rule.port_range(None) is None

    def test_to_dict_has_required_keys(self):
        rule = FirewallRule(
            rule_id="test-05", source="test", priority=0, line_number=1,
            action=Action.ALLOW
        )
        d = rule.to_dict()
        for key in ("rule_id", "src_ip", "dst_ip", "protocol", "action", "priority"):
            assert key in d