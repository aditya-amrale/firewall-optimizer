class TestAWSSGParser:

    def setup_method(self):
        self.rules = AWSSGParser().parse_dict(AWS_SG_SAMPLE)

    def test_returns_firewall_rules(self):
        assert all(isinstance(r, FirewallRule) for r in self.rules)

    def test_all_inbound_are_allow(self):
        inbound = [r for r in self.rules if r.chain == "inbound"]
        assert all(r.action == Action.ALLOW for r in inbound)

    def test_outbound_rule_exists(self):
        outbound = [r for r in self.rules if r.chain == "outbound"]
        assert len(outbound) >= 1

    def test_http_rule_src_ip(self):
        http = next(r for r in self.rules if r.dst_port == "80")
        assert http.src_ip == "0.0.0.0/0"

    def test_https_expands_to_two_ipv4_rules(self):
        https_ipv4 = [
            r for r in self.rules
            if r.dst_port == "443" and "ipv4" in r.tags and r.chain == "inbound"
        ]
        assert len(https_ipv4) == 2

    def test_ipv6_rule_tagged(self):
        ipv6 = [r for r in self.rules if "ipv6" in r.tags]
        assert len(ipv6) >= 1
        assert ipv6[0].src_ip == "::/0"

    def test_sg_reference_rule(self):
        sg_ref = next(r for r in self.rules if "sg-reference" in r.tags)
        assert "sg:sg-0db456" in sg_ref.src_ip

    def test_all_traffic_outbound_has_no_port(self):
        outbound = next(r for r in self.rules if r.chain == "outbound")
        assert outbound.dst_port is None
        assert outbound.protocol == Protocol.ALL

    def test_sg_id_in_tags(self):
        assert any("sg:sg-0abc123" in r.tags for r in self.rules)

    def test_source_field(self):
        assert all(r.source == "aws_sg" for r in self.rules)


