# ═══════════════════════════════════════════════════════════════════════════════
# iptables tests
# ═══════════════════════════════════════════════════════════════════════════════

class TestIptablesParser:

    def setup_method(self):
        self.parser = IptablesParser()
        self.rules  = self.parser.parse_text(IPTABLES_SAMPLE)

    def test_parse_returns_list_of_firewall_rules(self):
        assert all(isinstance(r, FirewallRule) for r in self.rules)

    def test_correct_rule_count(self):
        # 6 -A lines with recognised targets (ESTABLISHED rule + SSH + HTTP + HTTPS + DNS + DROP)
        assert len(self.rules) == 6

    def test_ssh_rule_src_ip(self):
        ssh = next(r for r in self.rules if r.dst_port == "22")
        assert ssh.src_ip == "10.10.10.0/24"

    def test_ssh_rule_action(self):
        ssh = next(r for r in self.rules if r.dst_port == "22")
        assert ssh.action == Action.ALLOW

    def test_ssh_rule_comment(self):
        ssh = next(r for r in self.rules if r.dst_port == "22")
        assert ssh.comment == "mgmt-ssh"

    def test_http_rule_protocol(self):
        http = next(r for r in self.rules if r.dst_port == "80")
        assert http.protocol == Protocol.TCP

    def test_drop_rule_has_no_src_port(self):
        drop = next(r for r in self.rules if r.action == Action.DROP)
        assert drop.src_port is None

    def test_dns_rule_is_udp(self):
        dns = next(r for r in self.rules if r.dst_port == "53")
        assert dns.protocol == Protocol.UDP

    def test_source_field(self):
        assert all(r.source == "iptables" for r in self.rules)

    def test_priorities_are_sequential(self):
        priorities = [r.priority for r in self.rules]
        assert priorities == sorted(priorities)

    def test_skip_comments_and_table_headers(self):
        # No rule should have raw starting with # or *
        for r in self.rules:
            assert not (r.raw or "").startswith("#")
            assert not (r.raw or "").startswith("*")


