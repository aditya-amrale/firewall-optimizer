# Cisco ACL tests
# ═══════════════════════════════════════════════════════════════════════════════

class TestCiscoACLParser:

    def setup_method(self):
        self.rules = CiscoACLParser().parse_text(CISCO_ACL_SAMPLE)

    def test_returns_firewall_rules(self):
        assert all(isinstance(r, FirewallRule) for r in self.rules)

    def test_extended_acl_http_rule(self):
        http = next(r for r in self.rules if r.dst_port == "80" and r.chain == "OUTSIDE_IN")
        assert http.action == Action.ALLOW
        assert http.protocol == Protocol.TCP
        assert http.src_ip == "0.0.0.0/0"

    def test_dns_host_src(self):
        dns = next(r for r in self.rules if r.dst_port == "53")
        assert dns.src_ip == "8.8.8.8/32"

    def test_range_port_parsing(self):
        range_rule = next(
            r for r in self.rules if r.dst_port and "1024" in r.dst_port
        )
        assert range_rule.dst_port == "1024:65535"

    def test_deny_all_is_logged(self):
        deny_all = next(
            r for r in self.rules if r.action == Action.DENY and r.chain == "OUTSIDE_IN"
        )
        assert "logged" in deny_all.tags

    def test_standard_acl_no_proto(self):
        rfc_rules = [r for r in self.rules if r.chain == "RFC1918_BLOCK"]
        assert len(rfc_rules) == 4  # 3 deny + 1 permit
        assert all(r.protocol == Protocol.ALL for r in rfc_rules)

    def test_wildcard_to_cidr_conversion(self):
        # 192.168.0.0 / 0.0.255.255 → 192.168.0.0/16
        cidr_rule = next(
            r for r in self.rules if r.src_ip == "192.168.0.0/16"
        )
        assert cidr_rule is not None

    def test_numbered_acl_parsed(self):
        num_rules = [r for r in self.rules if r.chain and r.chain.startswith("acl-101")]
        assert len(num_rules) == 2

    def test_remark_becomes_comment(self):
        http_rule = next(
            r for r in self.rules
            if r.dst_port == "80" and r.chain == "OUTSIDE_IN"
        )
        assert http_rule.comment == "Allow public HTTP"


